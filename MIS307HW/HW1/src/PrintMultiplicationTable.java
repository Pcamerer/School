public class PrintMultiplicationTable
{
   public static void main(String[] args)
   {
      int a;
      int b;
      for (a=1; a<=10; ++a) {
    	  for (b=1; b<=10; ++b) {
    		  System.out.print(a*b+"  ");  
    	  }
    	  System.out.println();
      }
	  
	  
	  
	  
	  
   }
}